function showDesigner() {
    var div = document.getElementById("designer");
    if (div.style.display !== "none") {
        div.style.display = "none";
    } else {
        div.style.display = "block";
    }
}

function closeDeg() {
    var div = document.getElementById("designer");
    if (div.style.display !== "block") {
        div.style.display = "block";
    } else {
        div.style.display = "none";
    }
}

